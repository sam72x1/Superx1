"""أدوات نصّية مشتركة — هروب HTML لتيليجرام.

أي نص خارجي (عنوان خبر، اسم ناشر، مخرجات Claude) يُدمَج في رسالة
parse_mode=HTML **يجب** أن يُهرَّب، وإلا فمحرف < أو > أو & واحد يجعل تيليجرام
يرفض الرسالة (400) فتُفقَد كاملةً — أو يُحقَن وسم في البطاقة.
"""

from __future__ import annotations


def esc(s) -> str:
    """يهرّب & < > حتى لا تكسر (أو تُحقَن في) HTML تيليجرام."""
    return (str(s).replace("&", "&amp;")
            .replace("<", "&lt;").replace(">", "&gt;"))
